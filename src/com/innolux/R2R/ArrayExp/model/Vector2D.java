package com.innolux.R2R.ArrayExp.model;

public class Vector2D {
	double xAxis;
	double yAxis;
	double xValue;
	double yValue;

	public Vector2D(double xAxis, double yAxis, double xValue, double yValue){
		this.xAxis = xAxis;
		this.yAxis = yAxis;
		this.xValue = xValue;
		this.yValue = yValue;
	}
}
